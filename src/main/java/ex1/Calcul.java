package ex1;

/**
 * Classe qui realise des operations arythmetiques
 * @author DIGINAMIC
 */
public class Calcul {

	/** Methode qui retourne l'addition des parametres a et b 
	 * 
	 * @param a de type int
	 * @param b de type int
	 * @return int 
	 */
	public int Addition(int a, int b){
		return a+b;
	}
	
	/**Methode qui retourne la soustraction des parametres a et b 
	 * 
	 * @param a de type int
	 * @param b de type int
	 * @return int
	 */
	public int Soustraction(int a, int b){
		return a-b;
	}
}
